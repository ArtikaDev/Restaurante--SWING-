    // Valida que haya 4  comas idenpendientemente del contenido que se asume que es valido
    
    private boolean platosValidos(){
        Pattern patt = Pattern.compile("([^,]*,){3}[^,]*");
        boolean numPlatosValido = true;
        for (int i = 0; i < numComensales && numPlatosValido; i++) {
            Matcher matcher = patt.matcher(textFields[i].getText());
            if(matcher.matches()){
                
            }else{
                numPlatosValido = false;
                JOptionPane.showMessageDialog(null, "Tienen que haber 4 platos en el Comensal " + (i + 1), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        return numPlatosValido;
    }
    
    private void guardarPlatos(){
        String[] platosComensales = new String[numComensales];
        for (int i = 0; i < numComensales; i++) {
            platosComensales[i] = textFields[i].getText();
        }
        ventanaReserva.setPlatosComensales(platosComensales);
    }
    
    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        if(platosValidos()){
            guardarPlatos();
            JOptionPane.showMessageDialog(null, "La acción se ha realizado correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            ventanaReserva.setButton(numComensales - 1);
            this.dispose();
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

