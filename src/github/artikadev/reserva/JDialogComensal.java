    // Funcion dedicada a leer el archivo menu
    
    private void leerMenu(){
        try (BufferedReader br = new BufferedReader(new FileReader("menu\\menu.txt"))) {
            String linea;
            while ((linea = br.readLine()) != null) {
                jTextAreaMenu.append(linea + "\n");
            }
        } catch (IOException e) {
            e.getMessage();
            jTextAreaMenu.setText("Error al leer el archivo");
            System.out.println(e);
        }
    }
    
